package com.paradigmcreatives.ccl;

public class SliderData {

	String slide_id ;
	String slide_url;
	String slide_title ;
	String slide_album_id ;
	public String getSlide_id() {
		return slide_id;
	}
	public void setSlide_id(String slide_id) {
		this.slide_id = slide_id;
	}
	public String getSlide_url() {
		return slide_url;
	}
	public void setSlide_url(String slide_url) {
		this.slide_url = slide_url;
	}
	public String getSlide_title() {
		return slide_title;
	}
	public void setSlide_title(String slide_title) {
		this.slide_title = slide_title;
	}
	public String getSlide_album_id() {
		return slide_album_id;
	}
	public void setSlide_album_id(String slide_album_id) {
		this.slide_album_id = slide_album_id;
	}
}
