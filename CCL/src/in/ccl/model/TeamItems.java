package in.ccl.model;

public class TeamItems {

	private int teamPlayersResource;

	private int teamDetailsResource;

	public int getTeamPlayersResource () {
		return teamPlayersResource;
	}

	public void setTeamPlayersResource (int teamPlayersResource) {
		this.teamPlayersResource = teamPlayersResource;
	}

	public int getTeamDetailsResource () {
		return teamDetailsResource;
	}

	public void setTeamDetailsResource (int teamDetailsResource) {
		this.teamDetailsResource = teamDetailsResource;
	}

}
